CREATE OR REPLACE transient TABLE SNOWFLAKE_LEARNING_DB.PUBLIC.gold_customer_360 AS
WITH order_facts AS (
    SELECT
        customer_id,
        COUNT(*) AS total_orders,
        COUNT(DISTINCT order_id) AS unique_orders,
        SUM(order_value) AS total_order_value,
        SUM(discount_amount) AS total_discount_amount,
        AVG(order_value) AS avg_order_value,
        MAX(order_value) AS max_order_value,
        MIN(order_value) AS min_order_value,
        MIN(order_date) AS first_order_date,
        MAX(order_date) AS last_order_date,
        DATEDIFF(DAY, MIN(order_date), MAX(order_date)) AS order_span_days,
        SUM(quantity) AS total_items_purchased,
        AVG(quantity) AS avg_items_per_order,
        COUNT(DISTINCT category) AS unique_categories_purchased,
        COUNT(DISTINCT brand) AS unique_brands_purchased,
        SUM(CASE WHEN is_returned THEN 1 ELSE 0 END) AS total_returns,
        SUM(CASE WHEN is_cancelled THEN 1 ELSE 0 END) AS total_cancellations,
        SUM(refund_amount) AS total_refund_amount,
        ROUND(SUM(CASE WHEN is_returned THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS return_rate_pct,
        ROUND(SUM(CASE WHEN is_cancelled THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS cancellation_rate_pct,
        SUM(CASE WHEN payment_type = 'COD' THEN 1 ELSE 0 END) AS cod_orders,
        SUM(CASE WHEN payment_type = 'UPI' THEN 1 ELSE 0 END) AS upi_orders,
        SUM(CASE WHEN payment_type = 'Card' THEN 1 ELSE 0 END) AS card_orders,
        SUM(CASE WHEN payment_type = 'Wallet' THEN 1 ELSE 0 END) AS wallet_orders,
        ROUND(SUM(CASE WHEN cod_flag THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS cod_pct,
        SUM(CASE WHEN order_status = 'delivered' THEN 1 ELSE 0 END) AS delivered_orders,
        AVG(delivery_time_days) AS avg_delivery_days,
        AVG(customer_rating) AS avg_customer_rating,
        SUM(CASE WHEN high_rto_risk_flag THEN 1 ELSE 0 END) AS high_rto_risk_orders,
        SUM(CASE WHEN fraud_flag THEN 1 ELSE 0 END) AS fraud_flagged_orders,
        SUM(margin) AS total_margin,
        SUM(profit) AS total_profit,
        MODE(category) AS most_frequent_category,
