        ROUND(SUM(CASE WHEN click_flag THEN 1 ELSE 0 END) * 100.0 / NULLIF(SUM(CASE WHEN open_flag THEN 1 ELSE 0 END), 0), 2) AS click_rate_pct,
        ROUND(SUM(CASE WHEN conversion_flag THEN 1 ELSE 0 END) * 100.0 / NULLIF(SUM(CASE WHEN click_flag THEN 1 ELSE 0 END), 0), 2) AS conversion_rate_pct,
        SUM(conversion_value) AS total_marketing_conversion_value,
        AVG(time_to_open_seconds) AS avg_time_to_open_seconds,
        AVG(time_to_click_seconds) AS avg_time_to_click_seconds,
        AVG(engagement_score) AS avg_engagement_score,
        AVG(propensity_to_buy_score) AS avg_propensity_score,
        SUM(CASE WHEN unsubscribe_flag THEN 1 ELSE 0 END) AS unsubscribe_count,
        SUM(CASE WHEN spam_flag THEN 1 ELSE 0 END) AS spam_flag_count,
        AVG(fatigue_score) AS avg_fatigue_score,
        AVG(frequency_last_7_days) AS avg_freq_last_7_days,
        AVG(frequency_last_30_days) AS avg_freq_last_30_days,
        MODE(best_send_time_hour) AS best_send_time_hour,
        SUM(marketing_cost) AS total_marketing_cost,
        SUM(revenue_generated) AS total_marketing_revenue,
        ROUND(SUM(revenue_generated) / NULLIF(SUM(marketing_cost), 0), 2) AS marketing_roi,
        MODE(channel) AS preferred_channel,
        MODE(device_type) AS preferred_device_marketing,
        MAX(customer_segment) AS latest_customer_segment,
        MAX(customer_risk_segment) AS latest_risk_segment
    FROM SNOWFLAKE_LEARNING_DB.PUBLIC.marketing_interactions
    GROUP BY customer_id
)
