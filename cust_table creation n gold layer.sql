    SELECT
        customer_id,
        COUNT(*) AS total_marketing_interactions,
        COUNT(DISTINCT campaign_id) AS unique_campaigns_received,
        SUM(CASE WHEN channel = 'email' THEN 1 ELSE 0 END) AS email_interactions,
        SUM(CASE WHEN channel = 'whatsapp' THEN 1 ELSE 0 END) AS whatsapp_interactions,
        SUM(CASE WHEN channel = 'sms' THEN 1 ELSE 0 END) AS sms_interactions,
        SUM(CASE WHEN channel = 'push_notification' THEN 1 ELSE 0 END) AS push_interactions,
